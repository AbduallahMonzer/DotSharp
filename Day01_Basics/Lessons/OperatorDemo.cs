namespace DotSharp.Day01_Basics;

public static class OperatorDemo
{
    public static void ShowOperators()
    {
        int a = 10, b = 5;
        Console.WriteLine($"a + b = {a + b}");
        Console.WriteLine($"a - b = {a - b}");
        Console.WriteLine($"a * b = {a * b}");
        Console.WriteLine($"a / b = {a / b}");
        Console.WriteLine($"a % b = {a % b}");
    }
}
